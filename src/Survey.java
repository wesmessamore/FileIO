public class Survey {
    String name;
    String firstVisitOrNo;
    String scientificOrRecreational;
    String recommendFriendOrNo;
    String favoriteExhibit;
    String satisfactionRating;


    public Survey(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFirstVisitOrNo() {
        return firstVisitOrNo;
    }

    public void setFirstVisitOrNo(String firstVisitOrNo) {
        this.firstVisitOrNo = firstVisitOrNo;
    }

    public String getScientificOrRecreational() {
        return scientificOrRecreational;
    }

    public void setScientificOrRecreational(String scientificOrRecreational) {
        this.scientificOrRecreational = scientificOrRecreational;
    }

    public String getRecommendFriendOrNo() {
        return recommendFriendOrNo;
    }

    public void setRecommendFriendOrNo(String recommendFriendOrNo) {
        this.recommendFriendOrNo = recommendFriendOrNo;
    }

    public String getFavoriteExhibit() {
        return favoriteExhibit;
    }

    public void setFavoriteExhibit(String favoriteExhibit) {
        this.favoriteExhibit = favoriteExhibit;
    }

    public String getSatisfactionRating() {
        return satisfactionRating;
    }

    public void setSatisfactionRating(String satisfactionRating) {
        this.satisfactionRating = satisfactionRating;
    }


}
